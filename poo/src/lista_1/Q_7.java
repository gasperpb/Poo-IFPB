//Escreva um programa que entre com um número e o imprima caso seja maior do que 20.


package lista_1;

import java.util.Scanner;

public class Q_7 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		System.out.println("Adicione o  número");
		
		int n1 = sc.nextInt();
		
		if (n1>20) {
		System.out.println("O valor é maior que 20, valor ="+n1);}
	
	
	else {
		System.out.println("Adicione um valor maior que 20!");}
		
}}
	
		
	
	
		
		




